public class FindClosestNumber {

    public int findClosestNumber(int[] nums) {
        int ans = nums[0];
        for (int i = 1;i < nums.length;i++){
            if (nums[i] == 0){
                return 0;
            }
            if (Math.abs(ans) > Math.abs(nums[i])){
                ans = nums[i];
            }else if (Math.abs(ans) == Math.abs(nums[i])){
                ans = Math.max(ans,nums[i]);
            }
        }

        return ans;
    }
}
